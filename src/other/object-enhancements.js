/**
 * Gets the value at a specified path of a nested object.
 * @param {object} obj The object to query.
 * @param {string|string[]} path The path of the property to retrieve.
 * @param {*} [defaultValue=undefined] The value returned for undefined resolved values.
 * @returns {*} Returns the resolved value, else the defaultValue.
 */
const deepGet = (obj, path, defaultValue = undefined) => {
  const travel = (regexp) =>
    String.prototype.split
      .call(path, regexp)
      .filter(Boolean)
      .reduce((res, key) => (res !== null && res !== undefined ? res[key] : res), obj);
  const result = travel(/[,[\]]+?/) || travel(/[,[\].]+?/);
  return result === undefined || result === obj ? defaultValue : result;
};

/**
 * Creates an object with the same keys as the provided object and values generated by
 * running the provided function for each value.
 * @param {object} obj The object to iterate over.
 * @param {Function} fn The function to apply to each value.
 * @returns {object} Returns the new mapped object.
 */
const mapValues = (obj, fn) =>
  Object.keys(obj).reduce((acc, k) => {
    acc[k] = fn(obj[k], k, obj);
    return acc;
  }, {});

/**
 * Creates an object with keys and values inverted.
 * @param {object} obj The object to invert.
 * @returns {object} Returns the new inverted object.
 */
const invert = (obj) =>
  Object.keys(obj).reduce((acc, key) => {
    acc[obj[key]] = key;
    return acc;
  }, {});

/**
 * Performs a deep comparison between two values to determine if they are equivalent.
 * @param {*} a The value to compare.
 * @param {*} b The other value to compare.
 * @returns {boolean} Returns `true` if the values are equivalent, else `false`.
 */
const isEqual = (a, b) => {
  if (a === b) return true;

  if (a && b && typeof a === 'object' && typeof b === 'object') {
    if (a.constructor !== b.constructor) return false;

    let length, i, keys;
    if (Array.isArray(a)) {
      length = a.length;
      if (length !== b.length) return false;
      for (i = length; i-- > 0;) {
        if (!isEqual(a[i], b[i])) return false;
      }
      return true;
    }

    keys = Object.keys(a);
    length = keys.length;
    if (length !== Object.keys(b).length) return false;

    for (i = length; i-- > 0;) {
      if (!Object.prototype.hasOwnProperty.call(b, keys[i])) return false;
    }

    for (i = length; i-- > 0;) {
      const key = keys[i];
      if (!isEqual(a[key], b[key])) return false;
    }

    return true;
  }

  return a !== a && b !== b;
};


module.exports = { deepGet, mapValues, invert, isEqual };